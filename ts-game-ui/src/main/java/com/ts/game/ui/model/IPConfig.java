package com.ts.game.ui.model;

public class IPConfig {

    private String centralIp;
    private String teamServerIp;
    private String aIp;
    private String bIp;
    private String cIp;
    private String dIp;

    public IPConfig() {
	super();
    }

    public String getCentralIp() {
	return centralIp;
    }

    public void setCentralIp(String centralIp) {
	this.centralIp = centralIp;
    }

    public String getTeamServerIp() {
	return teamServerIp;
    }

    public void setTeamServerIp(String teamServerIp) {
	this.teamServerIp = teamServerIp;
    }

    public String getaIp() {
	return aIp;
    }

    public void setaIp(String aIp) {
	this.aIp = aIp;
    }

    public String getbIp() {
	return bIp;
    }

    public void setbIp(String bIp) {
	this.bIp = bIp;
    }

    public String getcIp() {
	return cIp;
    }

    public void setcIp(String cIp) {
	this.cIp = cIp;
    }

    public String getdIp() {
	return dIp;
    }

    public void setdIp(String dIp) {
	this.dIp = dIp;
    }

}
